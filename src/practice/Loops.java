package practice;

public class Loops {
    public static void main(String[] args) {

        int a = 0;
        while(a < 8) {
            System.out.println(a);
            a++;
        }

        int b = 0;
        do {
            System.out.println(b);
            b++;
        }
        while (b < 5);


        for (int c = 0; c < 5; c++) {
            System.out.println(c);
        }
    }


}
