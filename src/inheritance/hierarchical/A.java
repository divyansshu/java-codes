package inheritance.hierarchical;

 class A {
     public void printA() {
         System.out.println("class A");
     }
 }
 class B extends A{
        public void printB() {
            System.out.println("class B");
        }
 }

 class C extends A{
     public void printC() {
         System.out.println("class C");
     }
 }

 class D extends A{
     public void printD() {
         System.out.println("class D");
     }
 }


