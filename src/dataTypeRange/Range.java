package dataTypeRange;

public class Range {

    public static void main(String[] args) {

        //byte datatype range
        // -128 to 127
        byte a = -128;
        byte b = 127;
        System.out.println(a);
        System.out.println(b);

        //short data type range
        //-32768 to 32767
        short c = 32767;
        short d = -32768;
        System.out.println(c);
        System.out.println(d);

        //int datatype range
        // -2147483648 to 2147483647
        int e = 2147483647;
        int f = -2147483648;
        System.out.println(e);
        System.out.println(f);

        //long datatype range
        // -9223372036854775808 to 9,223,372,036,854,775,807
        long g = 9223372036854775807L;
        long h = -9223372036854775808L;
        System.out.println(g);
        System.out.println(h);

        //float datatype range
        //  -+3.402822347E+38F (6-7 decimal digits)
        float i = 9.987643f;
        System.out.println(i);

        //double datatype range
        // stores up to 15 decimal digits
        double j = 19.98765432101231d;
        System.out.println(j);

        //boolean datatype range
        //stores true or false
        boolean k = true;
        boolean l = false;
        System.out.println(k);
        System.out.println(l);

        //char datatype range
        //stores a single character/letter or ascii value
        char m = 'A';
        char n = 65;
        System.out.println(m);
        System.out.println(n);
        





    }
}
